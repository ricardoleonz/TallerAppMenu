package com.example.appmenuentretenido.culturageneral;

public class CulturaG {

    private String preguntaAL;
    private String respuestaPreguntas;

    public CulturaG(){
        this.preguntaAL = "";
        this.respuestaPreguntas="";
    }

    public String getPreguntaAL() {
        return preguntaAL;
    }

    public void setPreguntaAL(String preguntaAL) {
        this.preguntaAL = preguntaAL;
    }

    public String getRespuestaPreguntas() {
        return respuestaPreguntas;
    }

    public void setRespuestaPreguntas(String respuestaPreguntas) {
        this.respuestaPreguntas = respuestaPreguntas;
    }
}
